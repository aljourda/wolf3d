#include "libft.h"

int		ft_pow(int x, unsigned int y)
{
	int		tmp;

	if (y == 0)
		return (1);
	tmp = x;
	y--;
	while (y > 0)
	{
		tmp *= x;
		y--;
	}
	return (tmp);
}
