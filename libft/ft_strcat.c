#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char	*dst;

	dst = s1;
	while (*dst != 0)
		dst++;
	while (*s2 != 0)
		*dst++ = *s2++;
	*dst = 0;
	return (s1);
}
